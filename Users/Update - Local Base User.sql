/* Criando user na base local

Criar nas tabelas de usuarios do siebel do .dbf: S_USER, S_CONTANCT e etc.

Lembrando que para isso, é preciso logar no SQLDBX (ou qualquer outro client de acesso a dbf.) com user "SIEBEL". 
A senha do user siebel é a mesma do user local.
