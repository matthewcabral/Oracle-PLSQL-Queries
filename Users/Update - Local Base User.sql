IF NOT EXISTS (SELECT NAME FROM SYSUSERS WHERE NAME= 'VMITRAUD') execute sp_adduser VMITRAUD
GRANT CONNECT To "VMITRAUD" IDENTIFIED BY "VMITRAUD"
sp_changegroup SSE_ROLE, VMITRAUD
GRANT CONNECT TO VMITRAUD IDENTIFIED BY VMITRAUD
GRANT GROUP TO SSE_ROLE
GRANT MEMBERSHIP IN GROUP SSE_ROLE TO VMITRAUD

