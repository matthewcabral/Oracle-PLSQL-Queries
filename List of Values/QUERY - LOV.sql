    SELECT
        LOV.NAME
    FROM SIEBEL.S_LST_OF_VAL LOV
    WHERE LOV.NAME LIKE '%AA'
    OR LOV.NAME LIKE '%Fixo Movel'
    OR LOV.NAME LIKE 'Analista Vendas'
    OR LOV.NAME LIKE 'Ots%'
    OR LOV.NAME LIKE 'Assistente Indicação'
)
ORDER BY LOV.NAME ASC;