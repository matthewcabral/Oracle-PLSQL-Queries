	LOV.SUB_TYPE            AS "SUBTIPO",
	LOV.ORDER_BY            AS "ORDEM",
	LOV.LOW                 AS "BAIXO",
	LOV.HIGH                AS "ALTO",
	LOV.ACTIVE_FLG          AS "ATIVO",
	LOV.TRANSLATE_FLG       AS "TRADUZIR",
	LOV.MULTI_LINGUAL_FLG   AS "MULTILINGUE",
	LOV.RPLCTN_LVL_CD       AS "NIVEL_REPLICACAO",
	LOV.TARGET_LOW          AS "ALVO_BAIXO",
	LOV.TARGET_HIGH         AS "ALVO_ELEVADO"
	--LOV.WEIGHTING_FACTOR,
	--SLT.NAME,
	--LOV.DESC_TEXT,
	--LOV.PAR_ROW_ID,
	--LOV.LANG_ID
FROM SIEBEL.S_LST_OF_VAL LOV
LEFT JOIN SIEBEL.S_LST_OF_VAL PLOV ON PLOV.ROW_ID = LOV.PAR_ROW_ID
LEFT JOIN SIEBEL.S_LANG SLA ON SLA.LANG_CD = LOV.LANG_ID
LEFT JOIN SIEBEL.S_LIT SLT ON SLT.ROW_ID = LOV.BITMAP_ID
WHERE LOV.ACTIVE_FLG = 'Y'
AND LOV.TYPE = 'GVT_CHK_CLASS_POS_CANCEL_SALE'
